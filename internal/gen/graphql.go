package gen

import (
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"unicode"
)

// GraphQL adds api/graphql/<name>_stub.graphqls with a GraphQL type and Query field, then runs gqlgen generate.
func GraphQL(moduleRoot, rawName string, dryRun, skipGenerate bool) ([]string, error) {
	name := normalizeName(rawName)
	if name == "" {
		return nil, fmt.Errorf("invalid model name %q (use letters, digits, _ or -)", rawName)
	}
	pascal := snakeToPascal(name)
	schemaDir := filepath.Join(moduleRoot, "api", "graphql")
	schemaFile := filepath.Join(schemaDir, name+"_stub.graphqls")
	out := []string{schemaFile}
	if dryRun {
		return out, nil
	}
	if err := os.MkdirAll(schemaDir, 0o755); err != nil {
		return nil, err
	}
	if _, err := os.Stat(schemaFile); err == nil {
		return nil, fmt.Errorf("graphql stub already exists: %s (delete it or choose another name)", schemaFile)
	}
	if err := os.WriteFile(schemaFile, []byte(tmplGraphQLStub(lowerFirstRune(pascal), pascal)), 0o644); err != nil {
		return nil, err
	}
	if skipGenerate {
		out = append(out, "(skipped gqlgen generate: run go run github.com/99designs/gqlgen@v0.17.78 generate)")
		return out, nil
	}
	cmd := exec.Command("go", "run", "github.com/99designs/gqlgen@v0.17.78", "generate")
	cmd.Dir = moduleRoot
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		return out, fmt.Errorf("gqlgen generate failed: %w (stub written to %s)", err, schemaFile)
	}
	return out, nil
}

func tmplGraphQLStub(fieldName, typeName string) string {
	return fmt.Sprintf(`# Generated by zatrano gen graphql — implement resolvers then re-run: go run github.com/99designs/gqlgen@v0.17.78 generate

extend type Query {
	%s(id: ID!): %s
}

type %s {
	id: ID!
}
`, fieldName, typeName, typeName)
}

func lowerFirstRune(s string) string {
	if s == "" {
		return s
	}
	r := []rune(s)
	r[0] = unicode.ToLower(r[0])
	return string(r)
}
